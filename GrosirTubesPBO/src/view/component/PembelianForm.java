    private void resetForm() {
        bersihkanFieldProduk();
        lblTotal.setText("0");
        
        keranjang.clear();
        tableModel.setRowCount(0);
        
        idSupplierDipilih = 0;
    }
    
    
    private void bersihkanFieldProduk() {
        txtIdProduk.setText("");
        txtNamaProduk.setText("");
        txtNamaSupplier.setText("");
        txtHarga.setText("");
        txtJumlah.setText("");
    }
      
    private void updateTabel() {
        tableModel.setRowCount(0);
        int total = 0;
        
        for (DetailPembelian d : keranjang) {
            total += d.getSubTotal();
            Object[] row = {
                d.getIdProduk(),
                d.getNamaProduk(),
                d.getNamaSupplier(),
                df.format(d.getHargaSatuan()),
                d.getJumlah(),
                df.format(d.getSubTotal())
            };
            tableModel.addRow(row);
        }
        
        lblTotal.setText(df.format(total));
    }

    private int hitungTotalBelanja() {
        int total = 0;
        for (DetailPembelian d : keranjang) {
            total += d.getSubTotal();
        }
        return total;
    }
