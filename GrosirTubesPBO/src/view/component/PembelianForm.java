    private void btnCariProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariProdukActionPerformed
        JFrame parent = (JFrame) SwingUtilities.getWindowAncestor(this);
        PilihProdukDialog dialog = new PilihProdukDialog(parent, true, true); // true = mode pembelian
        dialog.setVisible(true);

        if (dialog.produkTerpilih != null) {
            Produk p = dialog.produkTerpilih;
            txtIdProduk.setText(String.valueOf(p.getIdProduk()));
            txtNamaProduk.setText(p.getNamaProduk());
            txtNamaSupplier.setText(p.getNamaSupplier());
            
            // Simpan ID supplier
            idSupplierDipilih = p.getIdSupplier();
            
            // Kosongkan field harga dan jumlah agar user mengisi manual
            txtHarga.setText("");
            txtJumlah.setText("");
            
            txtHarga.requestFocus();
        }
    }//GEN-LAST:event_btnCariProdukActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (txtIdProduk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih produk terlebih dahulu!");
            return;
        }
        if (txtHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan harga satuan!");
            return;
        }
        if (txtJumlah.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah!");
            return;
        }

        try {
            int id = Integer.parseInt(txtIdProduk.getText());
            String nama = txtNamaProduk.getText();
            String supplier = txtNamaSupplier.getText();
            int harga = Integer.parseInt(txtHarga.getText());
            int qty = Integer.parseInt(txtJumlah.getText());

            if (harga <= 0) {
                JOptionPane.showMessageDialog(this, "Harga harus lebih dari 0!");
                return;
            }
            if (qty <= 0) {
                JOptionPane.showMessageDialog(this, "Jumlah harus lebih dari 0!");
                return;
            }

            int subtotal = harga * qty;

            boolean ada = false;
            for(DetailPembelian d : keranjang) {
                if(d.getIdProduk() == id) {
                    d.setJumlah(d.getJumlah() + qty);
                    d.setSubTotal(d.getHargaSatuan() * d.getJumlah());
                    ada = true;
                    break;
                }
            }

            if(!ada) {
                DetailPembelian detail = new DetailPembelian(id, nama, supplier, qty, harga, subtotal);
                keranjang.add(detail);
            }

            updateTabel();
            bersihkanFieldProduk();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Format harga atau jumlah salah! Harus berupa angka.");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusItemActionPerformed
        int row = tblKeranjang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih item di keranjang yang akan dihapus!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus item ini dari keranjang?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            keranjang.remove(row);
            updateTabel();
        }
    }//GEN-LAST:event_btnHapusItemActionPerformed

    private void btnBatalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBatalActionPerformed
        if (keranjang.isEmpty()) {
            resetForm();
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin membatalkan transaksi ini?\nSemua item di keranjang akan dihapus.",
            "Konfirmasi Batal",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            resetForm();
        }
    }//GEN-LAST:event_btnBatalActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (keranjang.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Keranjang masih kosong!");
            return;
        }

        if (idSupplierDipilih == 0) {
            JOptionPane.showMessageDialog(this, "Supplier belum dipilih!");
            return;
        }

        int total = hitungTotalBelanja();

        int confirm = JOptionPane.showConfirmDialog(this,
            "Total Transaksi: Rp " + df.format(total) + "\nSimpan data pembelian ini?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            Pembelian p = new Pembelian();

            if (userLogin != null) {
                p.setIdUser(userLogin.getIdUser());
            } else {
                p.setIdUser(1); // Default user ID
            }

            p.setIdSupplier(idSupplierDipilih);
            p.setTotalHarga(total);
            p.setListDetail(keranjang);

            boolean sukses = pembelianDAO.simpanTransaksi(p);

            if (sukses) {
                JOptionPane.showMessageDialog(this, "Transaksi Pembelian Berhasil Disimpan!");
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Transaksi Gagal! Silakan coba lagi.");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

