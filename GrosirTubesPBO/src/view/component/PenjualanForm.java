    private void updateTabel() {
        tableModel.setRowCount(0);
        double total = 0;
        
        for (DetailPenjualan d : keranjang) {
            total += d.getSubtotal();
            Object[] row = {
                d.getIdProduk(),
                d.getNamaProduk(),
                df.format(d.getHargaSatuan()),
                d.getJumlah(),
                df.format(d.getSubtotal())
            };
            tableModel.addRow(row);
        }
        
        lblTotal.setText(df.format(total));
    }
    
    private int hitungTotalBelanja() {
        int total = 0;
        for (DetailPenjualan d : keranjang) {
            total += d.getSubtotal();
        }
        return total;
    }
    
    
